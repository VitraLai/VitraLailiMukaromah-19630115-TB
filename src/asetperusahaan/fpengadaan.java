    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
       String[] Fieldnya={"kode","tanggal","nama_barang","deskripsi","harga","jumlah","total"};
       String[] Valuenya = {kode.getText(),tgl.getText(),namabarang.getText(),desk.getText(),harga.getText(),jumlah.getText(),tot.getText()};
       if ( new config().DuplikasiPrimeryKey("pengadaan","kode",kode.getText()))
           {
            JOptionPane.showMessageDialog(null,"Kode Barang Sudah Ada");
            }else{
                 new config().Simpan("pengadaan",Fieldnya, Valuenya);
                 JOptionPane.showMessageDialog(null,"Data Berhasil di Simpan");
                 new config().setJudulTabel(tabel_pengadaan, JudulKolom);
                 new config().tampilTabel(tabel_pengadaan, JudulKolom, SQL);
                 new config().setLebarKolomTabel(tabel_pengadaan, LebarKolom);
       }
    }//GEN-LAST:event_simpanActionPerformed

