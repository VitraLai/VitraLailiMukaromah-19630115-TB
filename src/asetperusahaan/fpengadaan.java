    private void tabel_pengadaanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_pengadaanMouseClicked
        // TODO add your handling code here:
        int row = tabel_pengadaan.getSelectedRow();
        kode.setText(tabel_pengadaan.getModel().getValueAt(row,0).toString());
        tgl.setText(tabel_pengadaan.getModel().getValueAt(row,1).toString());
        namabarang.setText(tabel_pengadaan.getModel().getValueAt(row,2).toString());
        desk.setText(tabel_pengadaan.getModel().getValueAt(row,3).toString());
        harga.setText(tabel_pengadaan.getModel().getValueAt(row,4).toString());
        jumlah.setText(tabel_pengadaan.getModel().getValueAt(row,5).toString());
        tot.setText(tabel_pengadaan.getModel().getValueAt(row,6).toString());
    }//GEN-LAST:event_tabel_pengadaanMouseClicked

